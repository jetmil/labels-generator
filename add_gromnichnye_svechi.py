def get_category_id(cursor, category_name="Громничные свечи"):
    """Получить ID категории или создать новую"""
    cursor.execute("SELECT id FROM categories WHERE name = %s", (category_name,))
    result = cursor.fetchone()
    if result:
        return result[0]
    else:
        cursor.execute("INSERT INTO categories (name) VALUES (%s) RETURNING id", (category_name,))
        return cursor.fetchone()[0]

def add_candles(connection):
    """Добавить все свечи в базу данных"""
    cursor = connection.cursor()

    # Получить ID категории
    category_id = get_category_id(cursor, "Громничные свечи")
    print(f"Категория 'Громничные свечи' ID: {category_id}")

    # Получить максимальный sequence_number для новых записей
    cursor.execute("SELECT COALESCE(MAX(sequence_number), 0) FROM candles")
    max_seq = cursor.fetchone()[0]
    print(f"Текущий максимальный sequence_number: {max_seq}")

    added = 0
    updated = 0
    errors = 0

    for i, candle in enumerate(CANDLES_DATA, 1):
        sequence_number = max_seq + i  # Новый порядковый номер

        try:
            # Проверить, существует ли свеча
            cursor.execute("SELECT id FROM candles WHERE name = %s", (candle["name"],))
            existing = cursor.fetchone()

            # Формируем display_name с номером
            display_name = f"{sequence_number}. {candle['name']}"

            if existing:
                # Обновить существующую
                cursor.execute("""
                    UPDATE candles SET
                        category_id = %s,
                        sequence_number = %s,
                        display_name = %s,
                        tagline = %s,
                        description = %s,
                        practice = %s,
                        ritual_text = %s,
                        color = %s,
                        updated_at = CURRENT_TIMESTAMP,
                        last_modified_at = CURRENT_TIMESTAMP
                    WHERE name = %s
                """, (
                    category_id,
                    sequence_number,
                    display_name,
                    candle["tagline"],
                    candle["description"],
                    candle["practice"],
                    candle["ritual_text"].strip(),
                    candle["color"],
                    candle["name"]
                ))
                updated += 1
                print(f"[{sequence_number}] Обновлено: {candle['name']}")
            else:
                # Добавить новую
                cursor.execute("""
                    INSERT INTO candles (
                        category_id, sequence_number, display_name, name, tagline, description, practice, ritual_text, color, is_active
                    ) VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, true)
                """, (
                    category_id,
                    sequence_number,
                    display_name,
                    candle["name"],
                    candle["tagline"],
                    candle["description"],
                    candle["practice"],
                    candle["ritual_text"].strip(),
                    candle["color"]
                ))
                added += 1
                print(f"[{sequence_number}] Добавлено: {candle['name']}")

        except Exception as e:
            errors += 1
            print(f"[{i}] ОШИБКА при добавлении '{candle['name']}': {e}")
            connection.rollback()
            continue

    connection.commit()

    print(f"\n{'='*50}")
    print(f"ИТОГО:")
    print(f"  Добавлено: {added}")
    print(f"  Обновлено: {updated}")
    print(f"  Ошибок: {errors}")
    print(f"  Всего свечей: {len(CANDLES_DATA)}")

    return added, updated, errors

